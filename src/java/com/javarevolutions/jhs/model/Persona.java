package com.javarevolutions.jhs.model;

/**
 *
 * @author nelson
 */
public class Persona {
    
    private int codigo;
    private String nombre;

    public Persona() {
        
    }
    
    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
